package org.sergeys.cookbook.logic;

public class Recipe {
	
	private String hash;
	private String title;
	
	public String getHash() {
		return hash;
	}
	
	public void setHash(String hash) {
		this.hash = hash;
	}
	
	public String getTitle() {
		return title;
	}
	
	public void setTitle(String title) {
		this.title = title;
	}		
}
